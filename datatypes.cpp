#include <bits/stdc++.h>
using namespace std;
int main(){
    int a;
    a=12;
    cout<<"Size of int: "<<sizeof(a)<<endl;

    int b;
    char h='$';
    b='#';
    cout<<"Size of char: "<<sizeof(h)<<endl;
    cout<<"ASCII value: "<<b<<endl; //ASCII value of character is given as int


    bool c;
    c=true;
    cout<<"Size of boolean: "<<sizeof(c);

    return 0;
}